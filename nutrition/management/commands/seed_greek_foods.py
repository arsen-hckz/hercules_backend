class Command(BaseCommand):
    help = 'Seed the database with common Greek foods and accurate nutritional data'

    def handle(self, *args, **kwargs):
        created_count = 0
        skipped_count = 0

        for entry in GREEK_FOODS:
            name, brand, calories, protein, carbs, fat, fiber, sugars = entry
            off_id = f'greek-seed-{name.lower().replace(" ", "-")}-{brand.lower()}'

            _, created = FoodItem.objects.get_or_create(
                off_id=off_id,
                defaults={
                    'name': name,
                    'brand': brand,
                    'barcode': '',
                    'calories': calories,
                    'protein': protein,
                    'carbs': carbs,
                    'fat': fat,
                    'fiber': fiber,
                    'sugars': sugars,
                }
            )
            if created:
                created_count += 1
                self.stdout.write(f'  + {name}')
            else:
                skipped_count += 1

        self.stdout.write(self.style.SUCCESS(
            f'\nDone. {created_count} foods created, {skipped_count} already existed.'
        ))
